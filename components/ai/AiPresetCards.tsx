export function AiPresetPreviewSheet({
  preset,
  onClose,
}: {
  preset: AiGeneratedPreset;
  onClose: () => void;
}) {
  useEffect(() => {
    const handleKey = (e: KeyboardEvent) => {
      if (e.key === 'Escape') onClose();
    };
    document.addEventListener('keydown', handleKey);
    return () => document.removeEventListener('keydown', handleKey);
  }, [onClose]);

  return (
    <div
      style={{
        position: 'fixed',
        inset: 0,
        zIndex: 12000,
        display: 'flex',
        flexDirection: 'column',
        justifyContent: 'flex-end',
      }}
    >
      <div
        role="presentation"
        onClick={onClose}
        style={{
          position: 'absolute',
          inset: 0,
          backgroundColor: 'rgba(0,0,0,0.55)',
        }}
      />
      <div
        onClick={(e) => e.stopPropagation()}
        style={{
          position: 'relative',
          maxHeight: '78vh',
          overflowY: 'auto',
          backgroundColor: theme.colors.card,
          borderTop: `1px solid ${theme.colors.border}`,
          borderRadius: '16px 16px 0 0',
          padding:
            '16px 16px max(20px, env(safe-area-inset-bottom, 20px))',
        }}
      >
        <div
          style={{
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'space-between',
            marginBottom: 12,
          }}
        >
          <div
            style={{
              fontWeight: 700,
              fontSize: 17,
              color: theme.colors.textPrimary,
            }}
          >
            💪 {preset.name}
          </div>
          <button
            type="button"
            aria-label="Close preview"
            onClick={onClose}
            style={{
              background: 'none',
              border: 'none',
              color: theme.colors.textSecondary,
              fontSize: 22,
              cursor: 'pointer',
              padding: 4,
            }}
          >
            ✕
          </button>
        </div>
        <p
          style={{
            margin: '0 0 12px',
            fontSize: 13,
            color: theme.colors.textMuted,
          }}
        >
          Read-only preview · Generated by Alex
        </p>
        <ol
          style={{
            margin: 0,
            paddingLeft: 18,
            color: theme.colors.textSecondary,
            fontSize: 14,
            lineHeight: 1.55,
          }}
        >
          {preset.exercises.map((ex, i) => (
            <li key={`pv-${i}`} style={{ marginBottom: 8 }}>
              <strong style={{ color: theme.colors.textPrimary }}>
                {ex.name}
              </strong>
              {' — '}
              {ex.sets}×{ex.reps}
              {ex.restSeconds > 0 ? ` · ${ex.restSeconds}s rest` : ''}
            </li>
          ))}
        </ol>
        {preset.coachNote ? (
          <p
            style={{
              margin: '14px 0 0',
              fontSize: 13,
              color: theme.colors.textSecondary,
              lineHeight: 1.45,
            }}
          >
            💬 {preset.coachNote}
          </p>
        ) : null}
      </div>
    </div>
  );
}
