'use client';

import React, { useEffect } from 'react';
import { theme } from '@/lib/theme';
import type { AiGeneratedPreset } from '@/lib/api/client';

export type SaveUiState = 'idle' | 'saving' | 'saved' | 'error';

export function AiPresetResultCard({
  preset,
  saveState,
  onPreview,
  onSave,
}: {
  preset: AiGeneratedPreset;
  saveState: SaveUiState;
  onPreview: () => void;
  onSave: () => void;
}) {
  const busy = saveState === 'saving';
  const saved = saveState === 'saved';
  const failed = saveState === 'error';

  return (
    <div
      style={{
        marginTop: 10,
        padding: '12px 14px',
        borderRadius: 14,
        border: `1px solid ${theme.colors.border}`,
        backgroundColor: theme.colors.surface,
        maxWidth: '100%',
      }}
    >
      <div
        style={{
          fontWeight: 700,
          fontSize: 15,
          color: theme.colors.textPrimary,
          marginBottom: 4,
        }}
      >
        💪 {preset.name}
      </div>
      <div
        style={{
          fontSize: 12,
          color: theme.colors.textMuted,
          marginBottom: 10,
        }}
      >
        Generated by Alex
      </div>
      <ul
        style={{
          margin: '0 0 12px',
          paddingLeft: 16,
          color: theme.colors.textSecondary,
          fontSize: 13,
          lineHeight: 1.5,
        }}
      >
        {preset.exercises.map((ex, i) => (
          <li key={`${ex.name}-${i}`} style={{ marginBottom: 6 }}>
            <span style={{ color: theme.colors.textPrimary }}>• {ex.name}</span>
            <span style={{ color: theme.colors.textMuted, marginLeft: 8 }}>
              {ex.sets}×{ex.reps}
              {ex.restSeconds > 0 ? `  ${ex.restSeconds}s` : ''}
            </span>
          </li>
        ))}
      </ul>
      {preset.coachNote ? (
        <div
          style={{
            fontSize: 13,
            color: theme.colors.textSecondary,
            marginBottom: 12,
            padding: '8px 10px',
            borderRadius: 8,
            backgroundColor: 'rgba(255,255,255,0.04)',
            border: '1px solid rgba(255,255,255,0.06)',
          }}
        >
          💬 &quot;{preset.coachNote}&quot;
        </div>
      ) : null}
      <div style={{ display: 'flex', gap: 10, flexWrap: 'wrap' }}>
        <button
          type="button"
          onClick={onPreview}
          style={{
            padding: '8px 14px',
            borderRadius: 10,
            border: `1px solid ${theme.colors.border}`,
            backgroundColor: theme.colors.card,
            color: theme.colors.textPrimary,
            fontWeight: 700,
            fontSize: 13,
            cursor: 'pointer',
          }}
        >
          👁 Preview
        </button>
        <button
          type="button"
          onClick={onSave}
          disabled={busy || saved}
          style={{
            padding: '8px 14px',
            borderRadius: 10,
            border: `1px solid ${theme.colors.primary}`,
            backgroundColor: theme.colors.surface,
            color: theme.colors.primary,
            fontWeight: 700,
            fontSize: 13,
            cursor: busy || saved ? 'default' : 'pointer',
            opacity: busy ? 0.7 : 1,
          }}
        >
          {saved ? 'Saved! ✓' : busy ? 'Saving…' : '💾 Save Preset'}
        </button>
      </div>
      {failed && (
        <div
          style={{
            marginTop: 8,
            fontSize: 12,
            color: theme.colors.error,
            fontWeight: 600,
          }}
        >
          Save failed, try again
        </div>
      )}
    </div>
  );
}

